#include "libft.h"

int	ft_atoi(const char *str)
{
	long	i;
	long	j;
	long	n;

	i = 0;
	j = 0;
	n = 1;
	while (str[i] == '\t' || str[i] == '\n' ||
			str[i] == '\r' || str[i] == '\v' ||
			str[i] == '\f' || str[i] == ' ')
		i++;
	if (str[i] == '+' || str[i] == '-')
	{
		if (str[i] == '-')
			n = -1;
		i++;
	}
	while (str[i] >= '0' && str[i] <= '9')
	{
		j = j * 10;
		j = j + str[i] - '0';
		i++;
	}
	return (j * n);
}
